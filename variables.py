# Python Variables - Demonstration

# Creating variables
x = 5  # Integer variable
y = "John"  # String variable

print(x)  # Output: 5
print(y)  # Output: John

# Changing variable type
x = 4  # x is of type int
x = "Sally"  # x is now of type str
print(x)  # Output: Sally

# Casting variables to specific types
x = str(3)    # x will be '3' (string)
y = int(3)    # y will be 3 (integer)
z = float(3)  # z will be 3.0 (float)

# Checking data types
x = 5
y = "John"
print(type(x))  # Output: <class 'int'>
print(type(y))  # Output: <class 'str'>

# Using single or double quotes for strings
x = "John"
y = 'John'  # Both are the same

# Case-sensitivity in variable names
a = 4
A = "Sally"  # A is different from a
print(a)  # Output: 4
print(A)  # Output: Sally
